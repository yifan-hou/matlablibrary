function m = quat2SO3(q)
	q0 = q(1); q1 = q(2); q2 = q(3); q3 = q(4);
	q11 = q1*q1;
	q22 = q2*q2;
	q33 = q3*q3;
	q01 = q0*q1;
	q02 = q0*q2;
	q03 = q0*q3;
	% q10 = q1*q0;
	% q13 = q1*q3;
	q12 = q1*q2;
	q13 = q1*q3;
	% q20 = q2*q0;
	q23 = q2*q3;

	m = [1 - 2*q22 - 2*q33,	2*(q12 - q03),		2*(q13 + q02);
		 2*(q12 + q03),		1 - 2*q11 - 2*q33,	2*(q23 - q01);
		 2*(q13 - q02),		2*(q23 + q01),		1 - 2*q11 - 2*q22];
end